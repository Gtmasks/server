let root = window.oc_webroot

if (typeof root === "undefined") {
	root = location.pathname;
	const pos = root.indexOf('/index.php/');
	if (pos !== -1) {
		root = root.substr(0, pos);
	} else {
		root = root.substr(0, root.lastIndexOf('/'));
	}
}

export const webroot = root
